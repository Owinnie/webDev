// + --> addition; string concatenation
// - --> subtraction
// * --> multiplication
// / --> division
// % --> remainder : modulo

console.log(3 + 4);
console.log('One' + ', ' + 'two' + ', ' + 'three');   // One, two, three
console.log(4 - 3);
console.log(3 * 4);
console.log(4 / 2);
console.log(9 % 3);
